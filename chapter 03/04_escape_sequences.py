# ESCAPE SEQUENCES
# it comprises of more than one character but represent one character when used within a string.


# A - New line character
# 01 : This new line character print a sentence/characters/words in new line after that..
# 02 : The syntax is (* \n and then sentence  )

Name = "My name is syed ibad ali.\n I am good boy"
print(Name)


# B - Tab character
# 01 : it gives a tab space in output result
# 02 : syntax is same as new line

Name2 = "Ibad is a \t good boy."
print(Name2)


# other escape sequence characters are : \' , \\ , \r , \x  e.t.c..