# Dictionary

# 01 : Dictionary is a set of collection of key value pairs..
# 02 : Syntax => a = {"key" = "vaue", and so on}
# 03 : We can add list or tuples in dictionary
# 04 : We can create another dictionary in existing dictionary..
# 05 : It is unordered 
# 06 : It is mutable that can be change
# 07 : It can't contain duplicate values..
# 08 : It is indexed...
# 09 : we can add integer to dictionary..

mydict = {
    "fast" : "in a quick manner",
    "slow" : "lazy",
    "ibad" : "A coder"
}
print(mydict)

print(mydict['ibad']) # This will print a specific value of key in dictionary..
# The syntax is print(*variable['specific element])...