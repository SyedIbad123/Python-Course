# LIST METHODS


# A - Sort method
a  = [1,4,3,6,9,2,5,10,33,21,43,89,900] 
a.sort() # The sort method is used to sort numbers in ascending order
print(a)


# B - Reverse method
b  = [1,4,3,6,9,2,5,10,33,21,43,89,900] 
b.reverse() # The reverse method is used to reverse numbers in reverse order
print(b)


# C - Append method
b  = [1,4,3,6,9,2,5,10,33,21,43,89,900] 
b.append(233) # The append method is used to add a number in the end of the list
print(b)


# D - Insert method
b  = [1,4,3,6,9,2,5,10,33,21,43,89,900] 
b.insert(4,6) # The insert method is used to add a number (6) at the place of index(4)
# Here first number is index and the second number is the value that is inserted..
print(b)


# E - Pop method
b  = [1,4,3,6,9,2,5,10,33,21,43,89,900] 
b.pop() #  The pop method is used to remove the last element of list. if we give index as a argument it removes the element at that index.
print(b)


# F - Remove method
b  = [1,4,3,6,9,2,5,10,33,21,43,89,900] 
b.remove(21) #  The remove method is used to remove the given element from list
print(b)


# G - Index method
b = [1,2,3,4,5,6,7,8,9]
print(b.index(1)) # Here argument is given for a number to find index
