# LAMBDA FUNCTION IN PYTHON...
# 01 : This function is created by using lambda keyword.
# 02 : A lambda function is a small anonymous function.
# 03 : A lambda function can take any number of arguments, but can only have one expression.


# EXAMPLE NO : 01.
x = lambda a : a + 10
print(x(5))


# EXAMPLE N0 : 02.
x = lambda a, b : a * b
print(x(5, 6))


# EXAMPLE NO : 03.
x = lambda a, b, c : a + b + c
print(x(5, 6, 2))
