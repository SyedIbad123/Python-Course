# EXCEPTION HANDLING IN PYTHON...
# 01 : Exceptions are raised when the program is syntactically correct, but the code resulted in an error. This error does not stop the execution of the program, however, it changes the normal flow of the program.
# 02 : We use exception handling in python to make our program intelligent.
# 03 : Exceptions get triggered automatically on finding errors in Python.
# 04 : Exceptions allow us to jump out of random, illogical large chunks of codes in case of errors. 