# FILES HANDLING IN PYTHON..

# 01 : Files are playing imp role in python programming.
# 02 : Python supports file handling and allows users to handle files i.e., to read and write files, along with many other file handling options, to operate on files.
# 03 :  Python treats files differently as text or binary and this is important. Each line of code includes a sequence of characters and they form a text file. 
# 04 : Each line of a file is terminated with a special character, called the EOL or End of Line characters like comma (,) or newline character. It ends the current line and tells the interpreter a new one has begun. 
# 05 : There are two types of files in python.
#  text files(.txt,.c etc)
#  Binary file(.jpg,.dat etc)



