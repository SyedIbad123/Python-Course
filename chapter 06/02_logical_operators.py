# Logical Operators..
# 01 : AND Oerator (*if both conditions are true then print)
# 02 : OR Operator (*if one condition is true then print)
# 03 : NOT operator (* this reverses the condition),{it's always use with one arguement}


# AND operator
age = 90
if(age>30 and age<50):
    print("You are okay")
else:
    print("You are not okay")
    

    
# OR operator
age = 90
if(age>30 or age<50):
    print("You are okay")
else:
    print("You are not okay")
    
    
    
# NOT operator
age = 20
if(not age>10):
    print("yes")
else:
    print("no")