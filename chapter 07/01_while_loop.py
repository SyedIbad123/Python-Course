# While loop...
# 01 : It is used for infinite condition
'''02 : The syntax of while loop is => 
        while condition :   # (semicolon is mendatory)
           body of loop'''
# 03 : in while loop, the condition is checked first. if it evaluates to true, the body of loops executed,otherwise not!...
# 04 : If the loop is entered, the process of [condition check and execution] is continued until the condition becomes false..  
# 05 : if the condition never becomes false loop will execute infinite time..


    
i = 0
while i<10:        # This condition prints yes 10 times..
    print("yes")
    i += 1         # If this condition is not written than while loop is iterate infinite time..



# program to print a numbers between 1 to 500
i = 1
