# CLASS IN OOOPs.

# 01 : A class is a collection of objects.
# 02 : Class contains the blueprints or the prototype from which the objects are being created. It is a logical entity that contains some attributes and methods. 
# 03 : Classes are created by keyword (class).
# 04 : Attributes are the variables that belong to a class.
# 05 : Attributes are always public and can be accessed using the dot (.) operator.
# 06 : Class can always be created  by Pascal case(means first letter is always capital).
''' The syntax for creating class is:
            class ClassName:
               # Statement-1
                 .
                 .
                 .
               # Statement-N'''
               

class MyClass:
    x = 5
    
print(MyClass.x)   # Prints the value of x...

